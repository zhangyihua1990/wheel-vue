<template>
  <div class="wrapper" :class="toastClassed">
    <div class="toast" ref="toast">
      <div class="message">
        <slot v-if="!enableHtml"></slot>
        <div v-else v-html="$slots.default[0]"></div>
      </div>
      <div class="line" ref="line">
        <div class="close" v-if="closeButton" @click="onClickClose">
          {{closeButton.text}}
        </div>
      </div>
    </div>
  </div>
</template>

<script>
  export default {
    name: 'wheel-toast',
    props: {
      autoClose: {
        type: Boolean,
        default: false,
      },
      autoCloseDelay: {
        type: Number,
        default: 3,
      },
      closeButton: {
        type: Object,
        default() {
          return {
            text: '关闭',
            callback: undefined,
          };
        },
      },
      enableHtml: {
        type: Boolean,
        default: false,
      },
      position: {
        type: String,
        default: 'top',
        validator(value) {
          return ['top', 'bottom', 'middle'].indexOf(value) >= 0;
        },
      },
    },
    mounted() {
      this.execAutoClose();
      this.updateStyles();
    },
    computed: {
      toastClassed() {
        return {
          [`position-${this.position}`]: true,
        };
      },
    },
    methods: {
      updateStyles() {
        this.$nextTick(() => {
          this.$refs.line.style.height = `${this.$refs.toast.getBoundingClientRect().height}px`;
        });
      },
      execAutoClose() {
        if (this.autoClose) {
          setTimeout(() => {
            this.close();
          }, this.autoCloseDelay * 1000);
        }
      },
      close() {
        this.$el.remove();
        //必须在remove之后，destroy 之前
        this.$emit('close');
        this.$destroy();
      },
      onClickClose() {
        this.close();
        if (this.closeButton && typeof this.closeButton.callback === 'function') {
          this.closeButton.callback();
        }
      },
    },
  };
</script>

